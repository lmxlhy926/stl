'''
1、与字符串一样,元组的元素不能修改。
2、元组也可以被索引和切片,方法一样。
3、注意构造包含 0 或 1 个元素的元组的特殊语法规则。
4、元组也可以使用+操作符进行拼接。
'''
tup1 = ()                   #0个元素
tup2 = (20,)                #1个元素
tup3 = (1, 2, 3, 4, 5, 6)   #多个元素
print(tup1)
print(tup2)
print(tup3)
print('----------------------------------------')

